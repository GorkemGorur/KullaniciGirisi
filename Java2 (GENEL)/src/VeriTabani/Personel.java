public void TabloYenile(){
    try {
        statement=con.createStatement();
        rs=statement.executeQuery("Select * from personel");
        TabloTemizle();
        model=(DefaultTableModel)tablo.getModel();
        while(rs.next()){
            model.addRow(new Object[]{rs.getString("tcno"),rs.getString("ad"),rs.getString("s"),rs.getString("bolum")});
        }
    } catch (Exception e) {
    }    
}
public void TabloTemizle(){
    model=(DefaultTableModel)tablo.getModel();
    model.getDataVector().removeAllElements();
}
    private void btn_GuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuncelleActionPerformed
        String sorgu="UPDATE personel set ad=?, s=?, bolum=? where tcno=?";
        try {
    
            PreparedStatement pst=con.prepareStatement(sorgu);
            pst.setString(1,txt_ad.getText());
            pst.setString(2,txt_soyad.getText());
            pst.setString(3,txt_bolum.getText());
            pst.setString(4,txt_tcno.getText());
            int s=pst.executeUpdate();
            if(s>0){
                JOptionPane.showMessageDialog(this,"Güncelleme Başarılı");
                TabloYenile();
            }
        } catch (SQLException e) {
        }  
    }//GEN-LAST:event_btn_GuncelleActionPerformed

