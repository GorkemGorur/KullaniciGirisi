    public void ComboGetir(){
        String sorgu="Select * from bolumler";
        DefaultComboBoxModel md1= new DefaultComboBoxModel();
        try {
            statement=con.createStatement();
            rs=statement.executeQuery(sorgu);
            while(rs.next()){
                md1.addElement(rs.getString("bolumad"));
            }
            cmb_bolum.setModel(md1);
        } catch (SQLException ex) {
            Logger.getLogger(Personel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
